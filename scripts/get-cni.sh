#!/bin/bash

mkdir -p /opt/cni

echo "Downloading CNI"
wget --show-progress --https-only --timestamping \
  "https://storage.googleapis.com/kubernetes-release/network-plugins/cni-amd64-0799f5732f2a11b329d9e3d51b9c8f2e3759f2ff.tar.gz"

tar -xvf cni-amd64-0799f5732f2a11b329d9e3d51b9c8f2e3759f2ff.tar.gz -C /opt/cni